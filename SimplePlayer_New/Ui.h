//主界面，可自由定义
void drawUi(int file_num, int total_num, int mode, int vol, bool status, String name_cache, int play_time)
{
  setFont_L;  //设置字体大小
  
  int play_min = play_time / 60;
  int play_sec = play_time % 60;

  u8g.drawXBMP(68, 0, 24, 18, modePic[mode]);  //显示播放模式图标
  u8g.drawXBMP(4, 38, 14, 14, playPic[status]);//显示播放状态图标

  u8g.setPrintPos(4, 16);
  if (file_num < 100) {
    u8g.print("0");
  }
  if (file_num < 10) {
    u8g.print("0");
  }
  u8g.print(file_num);
  u8g.print("/");
  if (total_num < 100) {
    u8g.print("0");
  }
  if (total_num < 10) {
    u8g.print("0");
  }
  u8g.print(total_num);

  u8g.setPrintPos(4, 32 );
  u8g.print(name_cache);

  u8g.setPrintPos(45, 48);
  if (play_min < 10) {
    u8g.print(0);
  }
  u8g.print(play_min);
  u8g.print(":");
  if (play_sec < 10) {
    u8g.print(0);
  }
  u8g.print(play_sec);

  u8g.setPrintPos(90, 48);
  u8g.print("V:");
  u8g.print(vol);
}

void UI(int file_num, int total_num, int mode, int vol, bool status, String name_cache, int play_time) {
  u8g.firstPage();
  do {
    drawUi(file_num, total_num, mode, vol, status, name_cache, play_time);
  }
  while (u8g.nextPage());
}

